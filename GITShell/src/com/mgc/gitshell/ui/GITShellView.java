    private void jTFInputKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTFInputKeyPressed
        int typedKey = evt.getKeyCode();
        if (typedKey == KeyEvent.VK_UP) {
            if (indexHistory > 0) {
                jTFInput.setText(execHistory.get(--indexHistory).toString());
            }
        }
        if (typedKey == KeyEvent.VK_DOWN) {
            if (indexHistory < execHistory.size()-1) {
                jTFInput.setText(execHistory.get(++indexHistory).toString());
            } else {
                jTFInput.setText("");
                indexHistory = execHistory.size();
            }
        }

    }//GEN-LAST:event_jTFInputKeyPressed

    private void jTFInputKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTFInputKeyTyped
        // TODO add your handling code here:
        char typedKey = evt.getKeyChar();
        if (typedKey == '\n') {
            try {
                String cmdText = jTFInput.getText();
                hostSession.execute(cmdText, this);
                execHistory.add(cmdText);
                indexHistory = execHistory.size();
                jTFInput.setText(null);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jTFInputKeyTyped

