    public void append(String resultLine) throws Exception {
        sb.append(resultLine);
        sb.append("\n");
        if (autoShow) {
            this.show();
        };
    }

    public void clear() throws Exception {
        sb = new StringBuffer();
        if (autoShow) {
            this.show();
        };
    }

